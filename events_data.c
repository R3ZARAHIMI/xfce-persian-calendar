int find_events(const CalendarEvent *arr, int arr_count,
                 int month, int day,
                 const CalendarEvent **out, int max_out) {
    int found = 0;
    for (int i = 0; i < arr_count && found < max_out; i++) {
        if (arr[i].month == month && arr[i].day == day) {
            out[found++] = &arr[i];
        }
    }
    return found;
}

