    fun toJdn(year: Int, month: Int, day: Int): Long {
        val yearIndex = year - SUPPORTED_START_YEAR - 1
        val monthIndex = month - 1
        if (yearIndex < 0 || yearIndex >= supportedYears || monthIndex !in 0..11) return -1L
        val index = yearIndex * 12 + monthIndex
        if (index < 0 || index >= months.size) return -1L
        val monthLen = months[index + 1] - months[index]
        if (day < 1 || day > monthLen) return -1L
        return SUPPORTED_START_JDN + months[index] + day - 1
    }

    internal fun fromJdn(jd: Long): DateTriplet? {
        if (jd !in SUPPORTED_START_JDN..<jdSupportEnd) return null
        val days = (jd - SUPPORTED_START_JDN).toInt()
        var index = days / 30
        while (index + 1 < months.size && months[index + 1] <= days) ++index
        val yearIndex = index / 12
        val month = index % 12
        val day = days - months[index]
        return DateTriplet(
            year = yearIndex + SUPPORTED_START_YEAR,
            month = month + 1,
            dayOfMonth = day + 1
        )
    }
